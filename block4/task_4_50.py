# Основные переменные
a = float(input("Введите сторону a внутреннего прямоугольника: "))
b = float(input("Введите сторону b внутреннего прямоугольника: "))
c = float(input("Введите сторону c внешнего прямоугольника: "))
d = float(input("Введите сторону d внешнего прямоугольника: "))

# Проверка положительности сторон
if a <= 0 or b <= 0 or c <= 0 or d <= 0:
    print("Все стороны должны быть положительными числами")
else:
    # Возможные ориентации внутреннего прямоугольника: (a, b) или (b, a)
    orientation1_possible = (a <= c and b <= d)  # без поворота
    orientation2_possible = (a <= d and b <= c)  # с поворотом на 90 градусов

    if orientation1_possible or orientation2_possible:
        print("Можно уместить прямоугольник")
    else:
        print("Нельзя уместить прямоугольник")