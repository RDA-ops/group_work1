# Ввод двузначного числа
number = int(input("Введите двузначное число: "))

# Проверяем, что число действительно двузначное (10-99)
if 10 <= number <= 99:
    # Находим цифры числа
    tens = number // 10      # цифра десятков
    units = number % 10      # цифра единиц
    
    # Сумма цифр
    digit_sum = tens + units
    
    # а) Является ли сумма его цифр двузначным числом?
    if 10 <= digit_sum <= 99:
        print(f"а) Сумма цифр ({digit_sum}) является двузначным числом.")
    else:
        print(f"а) Сумма цифр ({digit_sum}) не является двузначным числом (она однозначная).")
    
    # б) Больше ли числа а сумма его цифр?
    # Здесь "числа а" - видимо, имеется в виду само исходное число
    if digit_sum > number:
        print(f"б) Сумма цифр ({digit_sum}) больше самого числа {number}.")
    elif digit_sum < number:
        print(f"б) Сумма цифр ({digit_sum}) меньше самого числа {number}.")
    else:
        print(f"б) Сумма цифр ({digit_sum}) равна самому числу {number}.")
else:
    print("Ошибка: нужно ввести двузначное число (от 10 до 99).")