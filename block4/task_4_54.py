# Основные переменные
a1 = float(input("Сторона чемодана a1: "))
a2 = float(input("Сторона чемодана a2: "))
a3 = float(input("Сторона чемодана a3: "))
b1 = float(input("Сторона коробки b1: "))
b2 = float(input("Сторона коробки b2: "))
b3 = float(input("Сторона коробки b3: "))

# Сортируем размеры чемодана и коробки
suitcase = sorted([a1, a2, a3])
box = sorted([b1, b2, b3])

# Проверяем, помещается ли коробка в чемодан
if (box[0] <= suitcase[0] and
    box[1] <= suitcase[1] and
    box[2] <= suitcase[2]):
    print("Коробка помещается в чемодан. Пассажир может сэкономить на оплате.")
else:
    print("Коробка не помещается в чемодан. Сэкономить не удастся.")
    