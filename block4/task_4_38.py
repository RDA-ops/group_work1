# Основные переменные для задачи
# Координаты точки
x = float(input("Введите координату x: "))
y = float(input("Введите координату y: "))

# Условия попадания в область I (правый полукруг радиуса 2)
# Область ограничена: x >= 0 (правая половина) и x^2 + y^2 <= 4 (внутри круга)
# По условию: точка не попадает на границу, поэтому используем строгие неравенства
is_inside = (x > 0) and (x**2 + y**2 < 4)

# Вывод результата
if is_inside:
    print("Точка попадает в область I")
else:
    print("Точка не попадает в область I")
    